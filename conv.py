t = [[[2,1], [1,1]],
        [[1,1], [1,2]],
        [[1,2], [1,1]],
        [[1,1], [2,1]],

        [[2,1], [1,1]],
        [[1,1], [1,2]],
        [[2,2], [-1,1]],
        [[-1,1], [2,2]],
        [[2,-1], [2,1]],
        [[2,-1], [1,2]],
        [[1,-1], [2,2]],
        [[2,2], [1,-1]],
        [[-1,2], [2,1]],
        [[1,-1], [2,2]]]

for i in range(len(t)) :
    for y in range(len(t[i])) :
        for x in range(len(t[i][y])) :
            if t[i][y][x] == 1 :
                t[i][y][x] = 2
            elif t[i][y][x] == -1 :
                t[i][y][x] = 0
            elif t[i][y][x] == 0 :
                t[i][y][x] = 1
            elif t[i][y][x] == 2 :
                t[i][y][x] = 3

print(t)
